def is_saas_ui_goal(job: Dict, target: str = "") -> bool:
    """Return True when the job should produce a polished SaaS UI artifact."""

    if target == "api_backend":
        return False
    goal = " ".join(
        str(job.get(k) or "")
        for k in ("goal", "prompt", "description", "title", "build_kind")
    ).lower()
    if target == "static_site" and any(m in goal for m in ("saas", "product", "dashboard", "app")):
        return True
    return sum(1 for marker in _SAAS_INTENT_MARKERS if marker in goal) >= 2


def _product_name(goal: str) -> str:
    text = (goal or "").lower()
    if "flowdesk" in text:
        return "FlowDesk"
    if "finance" in text or "billing" in text:
        return "LedgerPilot"
    if "crm" in text or "sales" in text:
        return "RevenueOS"
    if "health" in text:
        return "CareOps"
    if "project" in text or "workflow" in text:
        return "SignalDesk"
    return "FlowDesk"


def _goal_excerpt(goal: str) -> str:
    goal = re.sub(r"\s+", " ", (goal or "").strip())
    return goal[:900] or "Design a beautiful modern SaaS product UI with clean design system, multiple pages, components, and responsive layout."


